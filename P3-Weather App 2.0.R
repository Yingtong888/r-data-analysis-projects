weather_report <- function(temps, datetimes) {

  #extract element
  date <- strsplit(datetimes[1], " ")[[1]][1]
  cat("Hourly temperatures on:", date, "\n")
  
  #hours vector
  hours <- substr(datetimes, 12, 13)
  
  #vector naming
  names(temps) <- hours
  
  #convert temperatures
  #R可以直接对整个vector做计算！
  temps_f <- round((temps * 9/5) + 32, digits = 2)
  
  #hourly text output
  for(i in 1:length(temps_f)){
    print(paste0("Temperature at ", hours[i], ": ", 
    temps[i], "°C (", temps_f[i], "°F)"))
  }
  
  #filter temperatures
  print("Temperatures over 20°C:")
  print(temps[temps > 20]) 
}

